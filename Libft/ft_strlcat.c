#include <stdio.h>

size_t ft_strlcat(char *dest, const char *src, size_t n)
{
	int i;

	i = 0;
	while (*dest != '\0')
	{
		dest++;
		i++;
	}
	while (n > 0)
	{
		*dest = *src;
		
	}
}